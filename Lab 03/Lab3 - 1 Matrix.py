import numpy as np

# Коэффициенты СЛАУ
A = np.array([[2, 7, -8, 6],
              [4, 4, 0, -7],
              [-1, -3, 6, 3],
              [9, -7, -2, -8]])

# Столбец свободных членов
b = np.array([-39, 41, 4, 113])

# Находим обратную матрицу коэффициентов
A_inv = np.linalg.inv(A)

# Решаем систему
x = np.dot(A_inv, b)

print("Решение СЛАУ методом обратной матрицы:")
for i in range(len(x)):
    print(f"x{i + 1} = {x[i]}")
